def fix_content(content):
    """修复内容中的乱码"""
    # 先替换多字符词组，再替换单字符
    # 按长度降序排序，确保先替换长的词组
    sorted_items = sorted(CHAR_MAP.items(), key=lambda x: len(x[0]), reverse=True)
    
    for old, new in sorted_items:
        if old in content:
            content = content.replace(old, new)
    
    return content

def main():
    # 读取文件
    with open('city/agents/planning_agent.py', 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 修复内容
    fixed_content = fix_content(content)
    
    # 保存修复后的文件
    with open('city/agents/planning_agent.py', 'w', encoding='utf-8') as f:
        f.write(fixed_content)
    
    print("修复完成！")

if __name__ == '__main__':
    main()
